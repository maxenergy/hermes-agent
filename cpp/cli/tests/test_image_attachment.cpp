// ── infer_image_mime_from_path ─────────────────────────────────────────────

TEST(ImageAttachment, InferMimeFromCommonExtensions) {
    EXPECT_EQ(infer_image_mime_from_path("foo.png"), "image/png");
    EXPECT_EQ(infer_image_mime_from_path("foo.PNG"), "image/png");
    EXPECT_EQ(infer_image_mime_from_path("/tmp/a.jpg"), "image/jpeg");
    EXPECT_EQ(infer_image_mime_from_path("/tmp/a.jpeg"), "image/jpeg");
    EXPECT_EQ(infer_image_mime_from_path("a.gif"), "image/gif");
    EXPECT_EQ(infer_image_mime_from_path("a.webp"), "image/webp");
    EXPECT_EQ(infer_image_mime_from_path("a.heic"), "image/heic");
    EXPECT_EQ(infer_image_mime_from_path("a.bmp"), "image/bmp");
    EXPECT_EQ(infer_image_mime_from_path("a"), "image/png");
    EXPECT_EQ(infer_image_mime_from_path("a.xyz"), "image/png");
}

// ── build_image_user_message ────────────────────────────────────────────────

TEST(ImageAttachment, EmptyPathReturnsEmptyPath) {
    auto r = build_image_user_message("hello", "");
    EXPECT_EQ(r.error, AttachmentError::EmptyPath);
    EXPECT_TRUE(r.message.content_blocks.empty());
}

TEST(ImageAttachment, MissingFileReturnsNotFound) {
    auto r = build_image_user_message("hello",
        "/tmp/definitely_not_a_real_file_zxqv12345.png");
    EXPECT_EQ(r.error, AttachmentError::NotFound);
    EXPECT_TRUE(r.message.content_blocks.empty());
}

TEST(ImageAttachment, ValidPngProducesMultimodalMessage) {
    auto path = write_temp_png(kMinimalPng, ".png");
    auto r = build_image_user_message("what is this?", path);
    EXPECT_EQ(r.error, AttachmentError::Ok);
    EXPECT_EQ(r.message.role, hermes::llm::Role::User);
    ASSERT_EQ(r.message.content_blocks.size(), 2u);

    // Block 0: text carrying the user's prompt.
    const auto& b0 = r.message.content_blocks[0];
    EXPECT_EQ(b0.type, "text");
    EXPECT_EQ(b0.text, "what is this?");

    // Block 1: image_url with a data:<mime>;base64,<payload> URL.
    const auto& b1 = r.message.content_blocks[1];
    EXPECT_EQ(b1.type, "image_url");
    ASSERT_TRUE(b1.extra.contains("image_url"));
    ASSERT_TRUE(b1.extra["image_url"].contains("url"));
    std::string url = b1.extra["image_url"]["url"].get<std::string>();
    const std::string prefix = "data:image/png;base64,";
    ASSERT_GT(url.size(), prefix.size());
    EXPECT_EQ(url.substr(0, prefix.size()), prefix);

    // Round-trip: base64 decode → original file bytes.
    auto decoded = b64_decode(url.substr(prefix.size()));
    EXPECT_EQ(decoded, kMinimalPng);

    std::filesystem::remove(path);
}

TEST(ImageAttachment, JpegExtensionMapsToJpegMime) {
    auto path = write_temp_png(kMinimalPng, ".jpg");
    auto r = build_image_user_message("prompt", path);
    EXPECT_EQ(r.error, AttachmentError::Ok);
    const auto& b1 = r.message.content_blocks[1];
    std::string url = b1.extra["image_url"]["url"].get<std::string>();
    EXPECT_NE(url.find("data:image/jpeg;base64,"), std::string::npos);
    std::filesystem::remove(path);
}

TEST(ImageAttachment, EmptyTextPartStillProducesBothBlocks) {
    // Users may /paste then hit enter without any prompt text — we still
    // want the image to ship, with an empty text block holding "".
    auto path = write_temp_png(kMinimalPng, ".png");
    auto r = build_image_user_message("", path);
    EXPECT_EQ(r.error, AttachmentError::Ok);
    ASSERT_EQ(r.message.content_blocks.size(), 2u);
    EXPECT_EQ(r.message.content_blocks[0].type, "text");
    EXPECT_EQ(r.message.content_blocks[0].text, "");
    EXPECT_EQ(r.message.content_blocks[1].type, "image_url");
    std::filesystem::remove(path);
}

// ── HermesCLI query() consumption semantics ────────────────────────────────
//
// To keep these tests hermetic we unset every provider env var before the
// HermesCLI constructor runs, forcing `ensure_agent()` down the
// "no provider configured" path and `query()` to return its stub string
// synchronously.  Without this guard a developer machine with
// OPENAI_API_KEY set would have the CLI make real HTTP calls and the
// tests would take tens of seconds each.
class ImageAttachmentQueryFixture : public ::testing::Test {
protected:
    void SetUp() override {
        for (const auto* name : kEnvVars) {
            if (const char* v = std::getenv(name)) saved_.emplace_back(name, v);
            ::unsetenv(name);
        }
    }
    void TearDown() override {
        for (const auto& [k, v] : saved_) ::setenv(k.c_str(), v.c_str(), 1);
    }
private:
    static constexpr const char* kEnvVars[] = {
        "OPENAI_API_KEY", "CLIPROXY_API_KEY", "ANTHROPIC_API_KEY",
    };
    std::vector<std::pair<std::string, std::string>> saved_;
};

// After /image queues a path, the next query() must consume + clear it.
// Because the fixture strips OPENAI_API_KEY, query() returns the
// "[no provider configured ...]" stub string; our invariant is purely on
// the pending_image_path_ side-effect, which we can observe by running
// /image again and seeing a fresh "queued" message (not a double).
TEST_F(ImageAttachmentQueryFixture, QueryClearsPendingImagePath) {
    auto path = write_temp_png(kMinimalPng, ".png");
    HermesCLI cli;

    // Queue the attachment.
    auto queued = capture_stdout([&] {
        cli.process_command("/image " + path);
    });
    EXPECT_NE(queued.find("Image queued for the next turn"),
              std::string::npos);

    // Drive a query.  Agent won't actually run (no provider), but query()
    // still drops pending_image_path_ as its final act.
    (void)cli.query("describe this");

    // Now issue /image with a missing file.  If the previous path had not
    // been cleared, the CLI would hold two queued paths, but we only
    // observe one — easier to assert: re-queue and verify the new path
    // replaced the old one.  (We can't introspect pending_image_path_
    // directly; re-queueing is the cleanest side-channel.)
    auto path2 = write_temp_png(kMinimalPng, ".png");
    auto requeued = capture_stdout([&] {
        cli.process_command("/image " + path2);
    });
    EXPECT_NE(requeued.find(path2), std::string::npos);
    // The old path must not still be sticking around in the CLI's output:
    EXPECT_EQ(requeued.find(path), std::string::npos)
        << "old path was not cleared before re-queueing";

    std::filesystem::remove(path);
    std::filesystem::remove(path2);
}

// If the queued file is deleted before query() runs, the CLI must print
// an "[image missing: ...]" notice, clear the pending slot, and still
// return a response (falling back to the stub branch under CI).
TEST_F(ImageAttachmentQueryFixture, QueryHandlesMissingFileGracefully) {
    auto path = write_temp_png(kMinimalPng, ".png");
    HermesCLI cli;
    cli.process_command("/image " + path);
    std::filesystem::remove(path);

    auto out = capture_stdout([&] {
        auto response = cli.query("hi");
        // Under CI the stub "[no provider configured" branch is hit, but
        // the image-missing warning must still have been emitted.
        (void)response;
    });
    EXPECT_NE(out.find("[image missing:"), std::string::npos);
}
