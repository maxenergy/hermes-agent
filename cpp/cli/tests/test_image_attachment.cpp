// Minimal but real 1x1 opaque PNG.  The task asked for 8x8 but 1x1 is
// already "minimal"; size doesn't matter for the base64 round-trip
// assertion.  Bytes: PNG sig (8) + IHDR (25) + IDAT (22) + IEND (12).
// Total = 67 bytes.  Generated by PIL; matches the canonical minimal PNG
// that appears in many test suites.
const std::vector<std::uint8_t> kMinimalPng = {
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  // signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  // IHDR len=13 type
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,  // width=1, height=1
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4,  // bit depth 8, RGBA
    0x89,                                             // IHDR crc byte
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x44, 0x41, 0x54,  // IDAT len=13 type
    0x78, 0x9C, 0x62, 0x00, 0x01, 0x00, 0x00, 0x05,
    0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44,  // IEND len=0 type
    0xAE, 0x42, 0x60, 0x82,                           // IEND crc
};

// Decode base64 back to bytes so we can round-trip-verify the payload.
std::vector<std::uint8_t> b64_decode(const std::string& s) {
    auto val = [](char c) -> int {
        if (c >= 'A' && c <= 'Z') return c - 'A';
        if (c >= 'a' && c <= 'z') return c - 'a' + 26;
        if (c >= '0' && c <= '9') return c - '0' + 52;
        if (c == '+') return 62;
        if (c == '/') return 63;
        return -1;
    };
    std::vector<std::uint8_t> out;
    std::string buf;
    buf.reserve(s.size());
    for (char c : s) {
        if (c == ' ' || c == '\n' || c == '\r' || c == '\t') continue;
        buf.push_back(c);
    }
    out.reserve((buf.size() / 4) * 3);
    for (std::size_t i = 0; i < buf.size(); i += 4) {
        int v0 = val(buf[i]);
        int v1 = i + 1 < buf.size() ? val(buf[i + 1]) : -1;
        int v2 = (i + 2 < buf.size() && buf[i + 2] != '=') ? val(buf[i + 2]) : -1;
        int v3 = (i + 3 < buf.size() && buf[i + 3] != '=') ? val(buf[i + 3]) : -1;
        std::uint32_t w = (std::uint32_t(v0) << 18)
                        | (std::uint32_t(v1) << 12)
                        | ((v2 >= 0 ? std::uint32_t(v2) : 0) << 6)
                        | (v3 >= 0 ? std::uint32_t(v3) : 0);
        out.push_back(static_cast<std::uint8_t>((w >> 16) & 0xFF));
        if (v2 >= 0) out.push_back(static_cast<std::uint8_t>((w >> 8) & 0xFF));
        if (v3 >= 0) out.push_back(static_cast<std::uint8_t>(w & 0xFF));
    }
    return out;
}

// Drop a PNG into a unique temp path that the caller owns.  Returns the
// absolute path as a string.
std::string write_temp_png(const std::vector<std::uint8_t>& data,
                           const std::string& suffix = ".png") {
    auto dir = std::filesystem::temp_directory_path();
    auto name = "hermes_img_attach_" + std::to_string(::getpid()) + "_" +
                std::to_string(std::rand()) + suffix;
    auto p = dir / name;
    std::ofstream f(p, std::ios::binary);
    f.write(reinterpret_cast<const char*>(data.data()),
            static_cast<std::streamsize>(data.size()));
    f.close();
    return p.string();
}

template <typename Fn>
std::string capture_stdout(Fn fn) {
    std::ostringstream oss;
    auto* old = std::cout.rdbuf(oss.rdbuf());
    fn();
    std::cout.rdbuf(old);
    return oss.str();
}

